package controllers;

import com.google.inject.Inject;
import com.google.inject.Singleton;
import kg.timur.ebean.dto.PersonDto;
import kg.timur.ebean.model.Person;
import kg.timur.ebean.service.PersonService;
import ninja.Result;
import ninja.Results;
import ninja.params.PathParam;

import java.util.List;


@Singleton
public class PersonController {

    @Inject
    PersonService personService;

    public Result list() {
        List<Person> result = personService.list();
        return Results.ok().render(result);
    }

    public Result create(PersonDto personDto) {
        personService.create(personDto);
        return Results.noContent();
    }

    public Result modify(@PathParam("id") Long id, PersonDto personDto) {
        personService.modify(id, personDto);
        return Results.noContent();
    }

    public Result get(@PathParam("id") Long id) {
        PersonDto result = personService.find(id);
        return result != null ? Results.ok().render(result) : Results.notFound();
    }

}
